package _01_TestNGIntro;

import org.testng.annotations.Test;

public class TestNGBasicExample {

    // A simple class to understand the fundamentals of TestNG.

    @Test
    public void testMethod1() {
        System.out.println("TestNG Method 1 executed!");  // Demonstrates how a TestNG method works.
    }

    @Test
    public void testMethod2() {
        System.out.println("TestNG Method 2 executed!");  // Shows that you can define and run multiple test methods in TestNG.
    }
}
